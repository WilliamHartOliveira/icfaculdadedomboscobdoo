package ic.faculdadedombosco.model;

/**
 *
 * @author Anderson
 */
public class Requisitante {
    
    private String requisitante_matricula;
    private String requisitante_status;
    private String requisitante_nome;
    private String requisitante_tipo;
    private String requisitante_telefone;

    public String getRequisitante_matricula() {
        return requisitante_matricula;
    }

    public void setRequisitante_matricula(String requisitante_matricula) {
        this.requisitante_matricula = requisitante_matricula;
    }

    public String getRequisitante_nome() {
        return requisitante_nome;
    }

    public void setRequisitante_nome(String requisitante_nome) {
        this.requisitante_nome = requisitante_nome;
    }

    public String getRequisitante_status() {
        return requisitante_status;
    }

    public void setRequisitante_status(String requisitante_status) {
        this.requisitante_status = requisitante_status;
    }

    public String getRequisitante_telefone() {
        return requisitante_telefone;
    }

    public void setRequisitante_telefone(String requisitante_telefone) {
        this.requisitante_telefone = requisitante_telefone;
    }

    public String getRequisitante_tipo() {
        return requisitante_tipo;
    }

    public void setRequisitante_tipo(String requisitante_tipo) {
        this.requisitante_tipo = requisitante_tipo;
    }

}
